def reconstruct_outputs(
    seed: dict,
    compressed_seed_text: str,
    domain: DomainType,
    mode: ResetMode,
    fixation_score: float = 0.0,
) -> list[str]:
    """
    하위 호환성 유지용 — outputs list 반환.
    실제로는 get_scenarios()를 사용하는 것을 권장.
    """
    scenarios = get_scenarios(domain, mode, fixation_score)
    return [s["summary"] for s in scenarios]


def get_scenarios(
    domain: DomainType,
    mode: ResetMode,
    fixation_score: float = 0.0,
) -> list[dict]:
    """
    경로형 시나리오 반환 (A안/B안/C안).
    soft 모드: A안/B안만 반환.
    fixation_score >= UNCERTAINTY_THRESHOLD: 보류 경로만 반환.
    """
    if fixation_score >= UNCERTAINTY_THRESHOLD:
        return [{
            "title": "보류 | 불확실성 과다",
            "summary": f"도메인 '{domain.value}': 불확실성이 임계값 이상입니다. 확정적 판단 없이 가설 탐색 상태를 유지합니다.",
            "reason": ["불확실성이 판단 가능 임계값을 초과함"],
            "conditions": ["추가 정보 수집 후 재판단"],
        }]

    templates = DOMAIN_SCENARIOS.get(domain.value, DOMAIN_SCENARIOS["general"])
    return templates[:2] if mode == ResetMode.soft else templates
